// Helper function to filter settlements based on search query
export function filterSettlements(query: string): string[] {
  if (!query || query.length < 1) return [];

  const normalizedQuery = query.trim();

  // Prioritize settlements that START with the query, then include those that contain it
  const startsWithMatches = israeliSettlements.filter((settlement) =>
    settlement.startsWith(normalizedQuery)
  );

  const containsMatches = israeliSettlements.filter(
    (settlement) =>
      !settlement.startsWith(normalizedQuery) &&
      settlement.includes(normalizedQuery)
  );

  return [...startsWithMatches, ...containsMatches].slice(0, 10);
}
